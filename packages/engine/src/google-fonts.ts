const ALL_FONT_NAMES = new Set(ALL_FONTS.map((f) => f.family));

const loadedPreviewFonts = new Set<string>();
const pendingPreviewFonts = new Set<string>();
const previewSubscribers = new Set<() => void>();

export function isFontPreviewReady(family: string): boolean {
  return loadedPreviewFonts.has(family);
}

export function subscribePreviewLoads(callback: () => void): () => void {
  previewSubscribers.add(callback);
  return () => {
    previewSubscribers.delete(callback);
  };
}

function notifyPreviewSubscribers(): void {
  for (const cb of previewSubscribers) cb();
}

let previewDebounceTimer: ReturnType<typeof setTimeout> | null = null;
let pendingBatch: string[] = [];

export function loadFontPreviews(families: string[]): void {
  const toLoad = families.filter(
    (f) => ALL_FONT_NAMES.has(f) && !loadedPreviewFonts.has(f) && !pendingPreviewFonts.has(f),
  );
  if (toLoad.length === 0) return;

  for (const f of toLoad) {
    pendingPreviewFonts.add(f);
    if (!pendingBatch.includes(f)) pendingBatch.push(f);
  }

  if (previewDebounceTimer) clearTimeout(previewDebounceTimer);

  previewDebounceTimer = setTimeout(() => {
    const batch = pendingBatch;
    pendingBatch = [];
    if (batch.length === 0) return;

    const familyParams = batch.map((f) => `family=${encodeURIComponent(f)}:wght@400`).join('&');
    const url = `https://fonts.googleapis.com/css2?${familyParams}&display=swap`;

    const link = document.createElement('link');
    link.rel = 'stylesheet';
    link.href = url;
    link.onload = () => {
      for (const family of batch) {
        loadedPreviewFonts.add(family);
        pendingPreviewFonts.delete(family);
      }
      notifyPreviewSubscribers();
    };
    link.onerror = () => {
      for (const family of batch) {
        pendingPreviewFonts.delete(family);
      }
    };
    document.head.appendChild(link);
  }, 80);
}
